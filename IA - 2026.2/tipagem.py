#python com tipagem forte, implicita e dinamica
#tipagem forte + implicita: voce nao pode mudar o tipo da variavel, o proprio python entende o tipo da variavel
numero = 10 #int
numero = "10" #string
mensagem = "O número é: " + numero 
print(mensagem)

#tipagem dinamica: voce nao precisa declarar o tipo da variavel, o proprio python entende o tipo da variavel